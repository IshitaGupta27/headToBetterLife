            <h3 class="Title2">Symptoms</h3>
            <p>
                Post-traumatic stress disorder symptoms may start within one month of a traumatic event, but sometimes symptoms may not appear until years after the event. These symptoms cause significant problems in social or work situations and in relationships. They can also interfere with your ability to go about your normal daily tasks.

                PTSD symptoms are generally grouped into four types: intrusive memories, avoidance, negative changes in thinking and mood, and changes in physical and emotional reactions. Symptoms can vary over time or vary from person to person.
                    
            </p>
            <br/>
            
<p>
   <b> Intrusive memories
</b>
<br/>
Symptoms of intrusive memories may include:


</p>
<br/>
<p>
    <ul>
        <li>Recurrent, unwanted distressing memories of the traumatic event
        </li>
        <li>Reliving the traumatic event as if it were happening again (flashbacks)
        </li>
        <li>Upsetting dreams or nightmares about the traumatic event
        </li>
        
        <li>Severe emotional distress or physical reactions to something that reminds you of the traumatic event

        </li>
 </ul>


    <br/>
    <p><b>Avoidance</b>
        <br/>
        Symptoms of avoidance may include:

    </p>
    <br/>
    <p>
        <ul>
             <li>Trying to avoid thinking or talking about the traumatic event
            </li>
            <li>Avoiding places, activities or people that remind you of the traumatic event
            </li>
            <li>Negative changes in thinking and mood

            </li>
            
        </ul>
    </p>

    <br/>
    <p><b>
        Symptoms of negative changes in thinking and mood may include:


    </b></p>
          
<br/>
<p>
    <ul>
        <li>Negative thoughts about yourself, other people or the world

        </li>
        <li>Hopelessness about the future

        </li>
        <li>Memory problems, including not remembering important aspects of the traumatic event </li>
        <li>Difficulty maintaining close relationships
        </li>
        <li>Feeling detached from family and friends
        </li>
        <li>Lack of interest in activities you once enjoyed
        </li>
        <li>Difficulty experiencing positive emotions
        </li>
        <li>
            Feeling emotionally numb

        </li>
        <li>Changes in physical and emotional reactions
        </li>
    </ul>
</p>

<br/>
    <p><b>
        Symptoms of changes in physical and emotional reactions (also called arousal symptoms) may include:


    </b>
</p>
          
<br/>
<p>
    <ul>
        <li>Being easily startled or frightened

        </li>
        <li>Always being on guard for danger

        </li>
        <li>Self-destructive behavior, such as drinking too much or driving too fast

        </li>
        <li>Trouble sleeping

        </li>
        <li>
            Trouble concentrating


        </li>
        <li>Irritability, angry outbursts or aggressive behavior
</li>
<li>
    Overwhelming guilt or shame



</li>
    </ul>
</p>
<br/>
<p>
    <b>For children 6 years old and younger, signs and symptoms may also include:</b>
</p>
<br/>
<p>
    <ul>
        <li>Re-enacting the traumatic event or aspects of the traumatic event through play
        </li>
        <li>
            Frightening dreams that may or may not include aspects of the traumatic event


        </li>
    </ul>
</p>


