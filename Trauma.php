            <h3 class="Title2">Description</h3>

            <p>
                According to the American Psychological Association (APA), trauma is “an emotional response to a terrible event like an accident, rape, or natural disaster.”
However, a person may experience trauma as a response to any event they find physically or emotionally threatening or harmful.


            </p>
              <br />
              <p>
                A traumatized person can feel a range of emotions both immediately after the event and in the long term. They may feel overwhelmed, helpless, shocked, or have difficulty processing their experiences. Trauma can also cause physical symptoms.
 </p>
              <br />
              <p>
                Trauma can have long-term effects on the person’s well-being. If symptoms persist and do not decrease in severity, it can indicate that the trauma has developed into a mental health disorder called post-traumatic stress disorder (PTSD).
Trauma and stressor-related disorders are a group of emotional and behavioral problems that may result from childhood traumatic and stressful experiences.


            </p>
              <br />
              
              <p>
                These traumatic and stressful experiences can include exposure to physical or emotional violence or pain, including abuse, neglect or family conflict. Observing a parent being treated violently, for example, can be a traumatic experience, as can being the victim of violence or abuse. Stressors such as parental separation or divorce or even more severe stressors such as emotional or physical neglect can cause problems when they are prolonged or not addressed by caring adults. Even a move or the birth of a sibling can be a stressor that can cause significant difficulties for some children.
<br/>
